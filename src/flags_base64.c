#include "ft_ssl.h"

unsigned int	base64_flag(t_ssl *ssl, char ***av)
{
	(void)av;
	if (ssl->f.a && (ssl->flag_error = FLAG_ERR4("-a")))
		return (0);
	return ((ssl->f.a = 1));
}
